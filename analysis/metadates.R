################################################################################
# This script creates the study dates (json file) to be called via a yaml action
################################################################################

################################################################################
# 0.0 Import libraries
################################################################################
library('jsonlite')
library('here')

################################################################################
# 0.1 Create directories for output
################################################################################
fs::dir_create(here::here("output"))

################################################################################
# 1 Create study_dates and write as json to output
################################################################################
study_dates <-
  list(
    studyend_date = "2022-04-01",
    pandemicstart_date = "2020-02-01",
    mid2018_date = "2018-08-01"
  )

write_json(study_dates, path = "output/study_dates.json", auto_unbox = TRUE, pretty=TRUE)